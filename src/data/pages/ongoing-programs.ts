export interface ActivityTypeGroup {
  activityType: string;
  programs: OngoingProgram[];
}

/**
 * Groups programs by primary Activity Type, largest group first — mirrors
 * the source's own "by Activity Type" framing (12 real categories). The 2
 * programs with no Activity Type bucket under "Unspecified" rather than
 * guessing a category.
 */
export function groupByActivityType(programs: OngoingProgram[]): ActivityTypeGroup[] {
  const map = new Map<string, OngoingProgram[]>();
  for (const p of programs) {
    const key = p.primaryActivityType || 'Unspecified';
    if (!map.has(key)) map.set(key, []);
    map.get(key)!.push(p);
  }
  return Array.from(map.entries())
    .map(([activityType, items]) => ({ activityType, programs: items }))
    .sort((a, b) => b.programs.length - a.programs.length);
}
